import java.util.*;

public class Input {
    static Scanner in = new Scanner(System.in);

    

    public static String takeString() {
        return in.next();
    }

    public static String takeLine() {
        return in.nextLine();
    }

    public static int takeInt() {
        return in.nextInt();
    }

    public static float takeFloat() {
        return in.nextFloat();
    }

    public static double takeDouble() {
        return in.nextDouble();
    }

    public static long takeLong() {
        return in.nextLong();
    }

}
